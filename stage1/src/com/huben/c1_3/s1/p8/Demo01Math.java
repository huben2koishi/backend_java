package com.huben.c1_3.s1.p8;

public class Demo01Math {
    public static void main(String[] args) {

        // public static double abs(double num) 返回绝对值
        System.out.println(Math.abs(-50));

        // public static double ceil(double num) 向上取整
        System.out.println(Math.ceil(1.25));

        // public static double floor(double num) 向下取整
        System.out.println(Math.floor(1.95));

        // public static double round(double num) 四舍五入
        System.out.println(Math.round(0.25));

    }
}
